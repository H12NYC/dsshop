import Network from '../utils/network.js'
import i18n from '@/utils/lang/index'
export default {
	detail(success, fail) {
		Network.setGetMessage('user',{},i18n.t('common.loading'), function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
	edit(data,success, fail) {
		Network.setPost('user', data, function(res) {
			success(res)
		}, function(res) {
			uni.showToast({
				title: res.message,
				icon: 'none',
				duration: 2000
			})
		})
	},
	notification(data,success, fail) {
		Network.setPost('user/notification', data, function(res) {
			success(res)
		}, function(res) {
			uni.showToast({
				title: res.message,
				icon: 'none',
				duration: 2000
			})
		})
	},
	cancel(data,success, fail){
		Network.setPost('cancel', data, function(res) {
			success(res)
		}, function(res) {
			uni.showToast({
				title: res.message,
				icon: 'none',
				duration: 2000
			})
		})
	},
	authorization(success, fail) {
		Network.setGet('authorization',{}, function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	}
};
